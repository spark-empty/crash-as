/* ============================ [ INCLUDES  ] ====================================================== */
#include "io.h"
/* ============================ [ MACROS    ] ====================================================== */
#define UART0DR 0x09000000
/* ============================ [ TYPES     ] ====================================================== */
/* ============================ [ DECLARES  ] ====================================================== */
/* ============================ [ DATAS     ] ====================================================== */
/* ============================ [ LOCALS    ] ====================================================== */
/* ============================ [ FUNCTIONS ] ====================================================== */
void uart_putc(unsigned char byte)
{
	writel(UART0DR, byte);
}
